enum TextFormat {
  /// Bold text formatting
  bold,

  /// Italic text formatting
  italic,

  /// Underline text formatting
  underline,

  /// Strikethrough text formatting
  strikethrough,

  /// Subscript text formatting
  subscript,

  /// Superscript text formatting
  superscript,

  /// Inline code formatting
  code,
}
